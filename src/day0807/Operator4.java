/**
 관계 연산자 : >, <, >=, <=, ==, !=
*/

class Operator4 {
	public static void main(String[] args) {
		int i = 8, j = 7, k = 8;
		
		System.out.println(i + "== " + k + " = " + (i == k));
		System.out.println(i + "== " + j + " = " + (i == j));
		
		System.out.println(i + "== " + k + " = " + (i != k));
		System.out.println(i + "== " + j + " = " + (i != j));
		
		
	}
}
